export class ErrorUtil {
  static newError(errorCode: ErrorCodes = ErrorCodes.BAD_REQUEST) {
      if (!(errorCode in ErrorCodes)) errorCode = ErrorCodes.BAD_REQUEST;
      return new Error(errorCode.toString());
  }
}